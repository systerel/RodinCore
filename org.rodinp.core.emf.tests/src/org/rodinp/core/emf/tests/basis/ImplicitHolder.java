package org.rodinp.core.emf.tests.basis;

import org.rodinp.core.IInternalElement;
import org.rodinp.core.IInternalElementType;
import org.rodinp.core.IRodinElement;
import org.rodinp.core.RodinCore;
import org.rodinp.core.basis.InternalElement;

/**
 * A class for internal elements that are supposed to get implicit children.
 */
public class ImplicitHolder extends InternalElement {

	public static final IInternalElementType<ImplicitHolder> ELEMENT_TYPE = RodinCore
			.getInternalElementType("org.rodinp.core.emf.tests.implicitHolder");

	public ImplicitHolder(String name, IRodinElement parent) {
		super(name, parent);
	}

	@Override
	public IInternalElementType<? extends IInternalElement> getElementType() {
		return ELEMENT_TYPE;
	}

}
